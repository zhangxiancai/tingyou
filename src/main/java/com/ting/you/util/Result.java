package com.ting.you.util;

public class Result {
    boolean temp;
    Object data;

    public boolean isTemp() {
        return temp;
    }

    public void setTemp(boolean temp) {
        this.temp = temp;
    }

    public Object getData() {
        return data;
    }

    public void setData(Object data) {
        this.data = data;
    }
    public void isTure() {
        this.temp = true;
    }
    public void isFalse() {
        this.temp = false;
    }
}
